n1 = float(input('Digite um número:'))
n2 = float(input('Digite outro número:'))

print(f'{n1} multiplicado por {n2} é igual a {n1*n2}')
print(f'{n1} dividido por {n2} é igual a {n1/n2}')
print(f'A divisão inteira de {n1} por {n2} é igual a {n1//n2}')
print(f' O modolo de {n1} e {n2} é igual a {n1%n2}')
print(f'{n1} elevado por {n2} é igual a {n1**n2}')